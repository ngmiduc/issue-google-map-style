@Component({
  selector: 'app-home',
  templateUrl: 'home.page.html',
  styleUrls: ['home.page.scss'],
  standalone: true,
  imports: [IonicModule],
  schemas: [CUSTOM_ELEMENTS_SCHEMA],
})
export class HomePage implements OnInit {
  constructor() {}

  private googleMap: any = null
  private mapType: 'terrain' | 'hybrid' = 'terrain'

  ngOnInit() {
    const init = async (): Promise<void> => {
      const apiKey = 'GOGLE_MAPS_API_KEY'

      this.googleMap = await GoogleMap.create({
        id: 'my-map', // Unique identifier for this map instance
        element: document.getElementById('map') as HTMLElement, // reference to the capacitor-google-map element
        apiKey: apiKey, // Your Google Maps API Key
        config: {
          tilt: 0,
          panControl: false,
          rotateControl: false,
          scaleControl: false,
          zoomControl: false,
          clickableIcons: false,
          mapTypeControl: false,
          disableDefaultUI: false,
          fullscreenControl: false,
          streetViewControl: false,
          center: {
            // The initial position to be rendered by the map
            lat: 33.6,
            lng: -117.9,
          },
          zoom: 8, // The initial zoom level to be rendered by the map
          styles: STYLES,
        },
      })
    }

    init()
  }

  changeMapType(type: 'terrain' | 'hybrid'): void {
    this.mapType = type
    this.googleMap.setMapType(
      type === 'terrain' ? MapType.Terrain : MapType.Hybrid
    )
  }
}
